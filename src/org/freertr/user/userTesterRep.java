package org.freertr.user;

/**
 * one tester replacer
 *
 * @author matecsaba
 */
public class userTesterRep {

    /**
     * source
     */
    protected final String src;

    /**
     * target
     */
    protected final String trg;

    /**
     * create instance
     *
     * @param s source
     * @param t target
     */
    public userTesterRep(String s, String t) {
        src = s;
        trg = t;
    }

}
